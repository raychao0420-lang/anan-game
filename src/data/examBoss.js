export function pickExamQuestions() {
  const pick = (cat, n) => {
    const pool = EXAM_QUESTIONS.filter(q => q.category === cat)
    return [...pool].sort(() => Math.random() - 0.5).slice(0, n)
  }
  return [
    ...pick('數學', 4),
    ...pick('社會', 2),
    ...pick('自然', 2),
    ...pick('國語', 2),
  ].sort(() => Math.random() - 0.5)
}

